#!/usr/bin/env node

// scripts/generate-slash-docs.mjs
//
// Generates a slash-command docs page for a consumer repo. The drift-scan
// requires every enabled slash command to appear (backtick-wrapped) in one of
// the known doc paths. Without this file, a fresh consumer install always
// fails drift-scan with "Slash command /X is enabled but undocumented" — a
// real friction point caught by the v1.0.8 sandbox install.
//
// Output: a single Markdown file consumed by check-slash-command-docs.mjs.
// Pure function `buildSlashDocs(commands)` is exported so the installer can
// generate it during `make pipeline-bootstrap` without shelling out.

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { slashCommands } from "./lib/slash-commands.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");

const HEADER = [
  "# Pipeline Core — Slash commands",
  "",
  "_This file is GENERATED by `scripts/generate-slash-docs.mjs` during install (and on demand via `make pipeline-generate-docs`). It exists so `pipeline-drift-scan` can verify every enabled slash command is documented. Edit `scripts/lib/slash-commands.mjs` upstream, then regenerate._",
  "",
  "All commands below are invoked by posting a comment on a GitHub issue or PR. The comment-commands workflow parses the body and dispatches the command if the actor and state allow it.",
  "",
].join("\n");

export function buildSlashDocs(commands = slashCommands) {
  const enabled = commands.filter((c) => c.status === "enabled");
  const parts = [HEADER];

  parts.push("## Commands\n");
  parts.push("| Command | Moves issue to | Allowed actors | Notes |");
  parts.push("| --- | --- | --- | --- |");
  for (const cmd of enabled) {
    const target = cmd.targetState || "—";
    const actors = (cmd.allowedActors || []).join(", ");
    const notes = (cmd.description || "").replace(/\|/g, "\\|");
    parts.push(`| \`${cmd.name}\` | ${target} | ${actors} | ${notes} |`);
  }

  parts.push("");
  parts.push("## Allowed states (per command)\n");
  for (const cmd of enabled) {
    const from = (cmd.allowedFrom || []).map((s) => `\`${s}\``).join(", ") || "(any)";
    parts.push(`- \`${cmd.name}\` — from ${from}`);
  }
  parts.push("");

  return parts.join("\n");
}

if (process.argv[1]?.endsWith("/generate-slash-docs.mjs")) {
  const outPath = process.argv[2] || join(REPO_ROOT, "docs", "pipeline-core.md");
  const content = buildSlashDocs();
  writeFileSync(outPath, content);
  process.stdout.write(`Wrote ${outPath} (${content.split("\n").length} lines)\n`);
}
